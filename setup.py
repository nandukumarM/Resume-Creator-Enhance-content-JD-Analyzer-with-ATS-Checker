#!/usr/bin/env python3
"""
Smart ATS Setup Script
Automates the setup process for the Smart ATS application
"""

import os
import sys
import subprocess
import secrets

def print_header(text):
    """Print formatted header"""
    print("\n" + "="*60)
    print(f"  {text}")
    print("="*60 + "\n")

def print_step(step, description):
    """Print step information"""
    print(f"[{step}] {description}")

def check_python_version():
    """Check if Python version is compatible"""
    print_step("✓", "Checking Python version...")
    version = sys.version_info
    if version.major < 3 or (version.major == 3 and version.minor < 8):
        print("❌ Error: Python 3.8 or higher is required")
        print(f"   Current version: {version.major}.{version.minor}.{version.micro}")
        sys.exit(1)
    print(f"   ✓ Python {version.major}.{version.minor}.{version.micro} detected")

def create_directories():
    """Create necessary directories"""
    print_step("📁", "Creating directories...")
    directories = ['uploads', 'static', 'templates']
    for directory in directories:
        if not os.path.exists(directory):
            os.makedirs(directory)
            print(f"   ✓ Created {directory}/")
        else:
            print(f"   ✓ {directory}/ already exists")

def create_env_file():
    """Create .env file with generated secret key"""
    print_step("🔑", "Setting up environment variables...")
    
    if os.path.exists('.env'):
        overwrite = input("   .env file already exists. Overwrite? (y/N): ").lower()
        if overwrite != 'y':
            print("   ⏭  Skipping .env creation")
            return
    
    # Generate secure secret key
    secret_key = secrets.token_hex(32)
    
    env_content = f"""# Smart ATS Environment Configuration
# Generated by setup script

# Flask secret key (keep this secret!)
SECRET_KEY={secret_key}

# Google Gemini API Key
# Get yours at: https://makersuite.google.com/app/apikey
GEMINI_API_KEY=your-gemini-api-key-here

# Application Settings
FLASK_ENV=development
FLASK_DEBUG=True
"""
    
    with open('.env', 'w') as f:
        f.write(env_content)
    
    print("   ✓ Created .env file with generated secret key")
    print("   ⚠  Remember to add your GEMINI_API_KEY to .env file!")

def install_dependencies():
    """Install Python dependencies"""
    print_step("📦", "Installing dependencies...")
    
    if not os.path.exists('requirements.txt'):
        print("   ❌ requirements.txt not found!")
        return False
    
    try:
        subprocess.check_call([
            sys.executable, "-m", "pip", "install", "-r", "requirements.txt"
        ])
        print("   ✓ All dependencies installed successfully")
        return True
    except subprocess.CalledProcessError:
        print("   ❌ Failed to install dependencies")
        return False

