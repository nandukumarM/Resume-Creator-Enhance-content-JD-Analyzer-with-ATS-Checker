def verify_files():
    """Verify all required files exist"""
    print_step("✅", "Verifying project structure...")
    
    required_files = {
        'app.py': 'Main application file',
        'requirements.txt': 'Dependencies list',
        '.env': 'Environment variables',
    }
    
    missing = []
    for file, description in required_files.items():
        if os.path.exists(file):
            print(f"   ✓ {file} - {description}")
        else:
            print(f"   ❌ {file} - {description} (MISSING)")
            missing.append(file)
    
    return len(missing) == 0

def print_next_steps():
    """Print instructions for next steps"""
    print_header("✨ Setup Complete!")
    
    print("📝 Next Steps:\n")
    print("1. Get your Google Gemini API key:")
    print("   → Visit: https://makersuite.google.com/app/apikey")
    print("   → Create an API key")
    print("   → Copy the key\n")
    
    print("2. Update your .env file:")
    print("   → Open .env in a text editor")
    print("   → Replace 'your-gemini-api-key-here' with your actual API key\n")
    
    print("3. Run the application:")
    print("   → python app.py")
    print("   → Open browser to http://localhost:5000\n")
    
    print("4. (Optional) Create virtual environment:")
    print("   → python -m venv venv")
    print("   → Windows: venv\\Scripts\\activate")
    print("   → macOS/Linux: source venv/bin/activate")
    print("   → pip install -r requirements.txt\n")
    
    print("📚 For more information, check README.md\n")
    print("🎉 Happy analyzing! - ATS Checker\n")

def main():
    """Main setup function"""
    print_header("🚀 ATS Checker - Setup Wizard")
    
    print("This script will set up the Smart ATS application on your system.\n")
    
    try:
        # Step 1: Check Python version
        check_python_version()
        
        # Step 2: Create directories
        create_directories()
        
        # Step 3: Create .env file
        create_env_file()
        
        # Step 4: Create .gitignore
        create_gitignore()
        
        # Step 5: Install dependencies
        install = input("\n❓ Install Python dependencies now? (Y/n): ").lower()
        if install != 'n':
            install_dependencies()
        else:
            print("   ⏭  Skipped dependency installation")
            print("   💡 Run 'pip install -r requirements.txt' manually later")
        
        # Step 6: Verify setup
        if verify_files():
            print("\n✅ All required files present!")
        else:
            print("\n⚠️  Some files are missing. Please check the output above.")
        
        # Step 7: Print next steps
        print_next_steps()
        
    except KeyboardInterrupt:
        print("\n\n⚠️  Setup interrupted by user")
        sys.exit(1)
    except Exception as e:
        print(f"\n❌ Setup failed with error: {str(e)}")
        sys.exit(1)

if __name__ == "__main__":
    main()